public class Assignment {
    public static void main(String[] args) {
        int a = 10; //assign
        a+=10; // a = a + 10 i.e 20
        System.out.println(a);
        a -= 5; // a = a - 15 i.e 15 because previously a's value was 20
        System.out.println(a);
        a *= 3; //a = a * 3 i.e 45
        System.out.println(a);
        a/= 15; //a = a / 15 i.e 3
        System.out.println(a);
    }
}
