public class Switch {
    public static void main(String[] args) {
        int number = 4;
        switch (number) {
            case 1:
                System.out.println("Chola-Samosa");
                break;
            case 2:
                System.out.println("Bun-Muska and chai");
                break;
            case 3:
                System.out.println("Chola bhatura");
                break;
            case 4:
                System.out.println("Non-veg overload pizza");
                break;
            default:
                System.out.println("Wake up.....");
                break;
        }
    }
}
